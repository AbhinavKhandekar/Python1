#Range Function/List function for Looping

print(list(range (10))) #print 0-9

print(list(range(5,10))) #print 5-9

#print odd number form 1-10 by using list/range function
print(list(range(1,10,2)))

#print only even num 1-10
print(list(range(2,10,2)))

#print reverse number from 10-1
print(list(range(10,0,-1)))